    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try{
            String query="INSERT INTO `course_management`(`course_n`, `sem`, `sub1`, `sub2`, `sub3`, `sub4`, `sub5`, `sub6`, `sub7`) VALUES (?,?,?,?,?,?,?,?,?)";
            pst=conn.prepareStatement(query);
            pst.setString(1,cname.getText());
            pst.setString(2,sem.getSelectedItem().toString());
            pst.setString(3,sn1.getText());
            pst.setString(4,sn2.getText());
            pst.setString(5,sn3.getText());
            pst.setString(6,sn4.getText());
            pst.setString(7,sn5.getText());
            pst.setString(8,sn6.getText());
            pst.setString(9,sn7.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Course Added");
            cname.setText("");
           // sem.setText("");
            sn1.setText("");
            sn2.setText("");
            sn3.setText("");
            sn4.setText("");
            sn5.setText("");
            sn6.setText("");
            sn7.setText("");
        }
        catch(HeadlessException |SQLException e)
        {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_addActionPerformed

