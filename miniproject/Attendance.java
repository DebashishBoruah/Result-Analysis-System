    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      try
      {
           String stu_id=sid.getText();
          String semester=sem.getText();
          pst=conn.prepareStatement("SELECT T.sem,T.sub1,T.sub2,T.sub3,T.sub4,T.sub5,T.sub6,T.sub7 FROM course_management T JOIN student_info S ON S.sem=T.sem WHERE S.sem='"+semester+"' AND S.sid='"+stu_id+"'");
          rs=pst.executeQuery();
          while(rs.next())
          {
             String s=rs.getString("T.sem");
             String s1=rs.getString("T.sub1");
              String s2=rs.getString("T.sub2");
               String s3=rs.getString("T.sub3");
                String s4=rs.getString("T.sub4");
                 String s5=rs.getString("T.sub5");
                  String s6=rs.getString("T.sub6");
                   String s7=rs.getString("T.sub7"); 
            sem.setText(s);
            sname1.setText(s1);
            sname2.setText(s2);
            sname3.setText(s3);
            sname4.setText(s4);
            sname5.setText(s5);
            sname6.setText(s6);
            sname7.setText(s7);
             
          }
          
          
      }catch(Exception e)
      {
          
      }
    }//GEN-LAST:event_jButton3ActionPerformed

