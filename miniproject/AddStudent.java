    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      String check=sid.getText();
      if(check.isEmpty())
      {
          JOptionPane.showMessageDialog(null,"No student id is inserted");
      }
      else{
      try
      {
      pst=conn.prepareStatement("SELECT * FROM `student_info` WHERE sid='"+check+"'");
      rs=pst.executeQuery();
      while(rs.next())
      {
          String n=rs.getString("name");
          String p=rs.getString("password");;
          String e=rs.getString("email");;
          String f=rs.getString("fname");;
          String b=rs.getString("branch");;
          String ph=rs.getString("phnumber");;
          name.setText(n);
          pass.setText(p);
          email.setText(e);
          fname.setText(f);
          branch.setText(b);
          phno.setText(ph);
      }
      }catch(Exception e)
      {
          System.out.println(e);
      }
      }
    }//GEN-LAST:event_jButton3ActionPerformed

