    {
        try
        {
           pst=conn.prepareStatement("SELECT * FROM student_marks"); 
           rs=pst.executeQuery();
           while(rs.next())
           {
               String s_id=rs.getString("sid");
               String seme=rs.getString("sem");
               String s1=rs.getString("marks1");
               String s2=rs.getString("marks2");
               String s3=rs.getString("marks3");
               String s4=rs.getString("marks4");
                String s5=rs.getString("marks5");
                String s6=rs.getString("marks6");
                String s7=rs.getString("marks7");
                 String t=rs.getString("total");
                  String p=rs.getString("percentage");
                   String c=rs.getString("cgpa");
                 String tbData[] = {s_id,seme,s1,s2,s3,s4,s5,s6,s7,t,p,c};
    DefaultTableModel tbModel =(DefaultTableModel)jTable1.getModel();
    tbModel.addRow(tbData);
           }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
        
    }

